const a = 7 // esse simbolo de = é uma atribuição
let b = 3

b += a //isso significa que vamos fazer uma atribuição na letra A
        // e junto com a atribuição estamos fazendo uma adição
        // b = b + a
console.log(b)
b -= 4 // b = b - 4
console.log(b)
b *= 2//b = b * 2
console.log(b)
b /= 2 // b = b / 2
console.log(b)
b %= 2 // b = b % 2
console.log(b)

